package Clases_Principales;

/**
 *
 * @author Lenovo
 */ 
public class clientes {

    private String Nombre;
    private String Snombre;
    private String Pa;
    private String Sa;
    private String Ni;
    private String Fecha;
    private String di;
    private String telefono;
    private String ce;

    public clientes() {
    }

    public clientes(String Nombre, String Snombre, String Pa, String Sa, String Ni, String Fecha, String di, String telefono, String ce) {
        this.Nombre = Nombre;
        this.Snombre = Snombre;
        this.Pa = Pa;
        this.Sa = Sa;
        this.Ni = Ni;
        this.Fecha = Fecha;
        this.di = di;
        this.telefono = telefono;
        this.ce = ce;
    }

    
    
    /**
     * @return the Nombre
     */
    public String getNombre() {
        return Nombre;
    }

    /**
     * @return the Snombre
     */
    public String getSnombre() {
        return Snombre;
    }

    /**
     * @return the Pa
     */
    public String getPa() {
        return Pa;
    }

    /**
     * @return the Sa
     */
    public String getSa() {
        return Sa;
    }

    /**
     * @return the Ni
     */
    public String getNi() {
        return Ni;
    }

    /**
     * @return the Fecha
     */
    public String getFecha() {
        return Fecha;
    }

    /**
     * @return the di
     */
    public String getDi() {
        return di;
    }

    /**
     * @return the telefono
     */
    public String getTelefono() {
        return telefono;
    }

    /**
     * @return the ce
     */
    public String getCe() {
        return ce;
    }

    /**
     * @param Nombre the Nombre to set
     */
    public void setNombre(String Nombre) {
        this.Nombre = Nombre;
    }

    /**
     * @param Snombre the Snombre to set
     */
    public void setSnombre(String Snombre) {
        this.Snombre = Snombre;
    }

    /**
     * @param Pa the Pa to set
     */
    public void setPa(String Pa) {
        this.Pa = Pa;
    }

    /**
     * @param Sa the Sa to set
     */
    public void setSa(String Sa) {
        this.Sa = Sa;
    }

    /**
     * @param Ni the Ni to set
     */
    public void setNi(String Ni) {
        this.Ni = Ni;
    }

    /**
     * @param Fecha the Fecha to set
     */
    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }

    /**
     * @param di the di to set
     */
    public void setDi(String di) {
        this.di = di;
    }

    /**
     * @param telefono the telefono to set
     */
    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    /**
     * @param ce the ce to set
     */
    public void setCe(String ce) {
        this.ce = ce;
    }

    

}
